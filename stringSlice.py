 




#slice
string="HAVE A GOOD DAY"
s1=slice(4,9)
s2=slice(2,7)
s3=slice(-2,-7,-1)

print("stringslicing")
print(string[s1])
print(string[s2])
print(string[s3])

#simple
b="hello,world"
print(b[2:6])

#start to slice
b="hello,world"
print(b[:5])

#slice to end
b="hello,world"
print(b[2:])

b="vvbbnnmmjjuu"
print(b[::2])

#negative indexing
b="hello world"
print(b[-5:-1])

#reverse string
a="happy journey"
print(a[::-1])







